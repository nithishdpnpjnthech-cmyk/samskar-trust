// Translation Engine
class Translator {
    constructor() {
        this.currentLang = this.getStoredLang();
    }

    getStoredLang() {
        return localStorage.getItem('site_lang') || 'en';
    }

    setStoredLang(lang) {
        localStorage.setItem('site_lang', lang);
        this.currentLang = lang;
    }

    translate(key) {
        const translations = TRANSLATIONS[this.currentLang] || TRANSLATIONS['en'];
        return translations[key] || TRANSLATIONS['en'][key] || key;
    }

    translatePage() {
        // Translate elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.translate(key);
            if (translation) {
                // If translation contains HTML tags (e.g., hero title with <br>), set innerHTML.
                // Otherwise use textContent to avoid any unexpected HTML parsing issues.
                const hasHtml = /<[^>]+>/g.test(translation);
                if (hasHtml) {
                    element.innerHTML = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });

        // Translate placeholders with data-i18n-placeholder attribute
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            const translation = this.translate(key);
            if (translation) {
                element.placeholder = translation;
            }
        });

        // Update language button text
        const langBtn = document.getElementById('language-btn');
        if (langBtn) {
            const langSpan = langBtn.querySelector('[data-i18n="language_label"]');
            if (langSpan) {
                langSpan.textContent = this.translate('language_label');
            }
        }

        // Update html lang attribute
        document.documentElement.lang = this.currentLang === 'kn' ? 'kn' : 'en';
    }

    switchLanguage(lang) {
        if (TRANSLATIONS[lang]) {
            this.setStoredLang(lang);
            this.translatePage();
            return true;
        }
        return false;
    }
}

// Create global translator instance
window.translator = new Translator();
